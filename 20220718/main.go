func numSubmatrixSumTarget(matrix [][]int, target int) int {
	total := 0
	row := len(matrix)
	if row == 0 {
		return 0
	}

	column := len(matrix[0])
	if column == 0 {
		return 0
	}

	var sumData [][]int
	for i := 0; i <= row; i++ {
		sumData = append(sumData, []int{})
		for j := 0; j <= column; j++ {
			sumData[i] = append(sumData[i], 0)
		}
	}

	for i := 1; i <= row; i++ {
		for j := 1; j <= column; j++ {
			sumData[i][j] = sumData[i-1][j] + sumData[i][j-1] - sumData[i-1][j-1] + matrix[i-1][j-1]
		}
	}

	for i := 1; i <= row; i++ {
		for j := 1; j <= column; j++ {
			for k := 1; k <= i; k++ {
				for l := 1; l <= j; l++ {
					sum := sumData[i][j] - sumData[i][l-1] - sumData[k-1][j] + sumData[k-1][l-1]
					if sum == target {
						total++
					}
				}
			}
		}
	}

	return total
}

